import Cocoa
import NovellaModel

class MoveLinkableViewCmd: UndoableCommand {
	let _node: LinkableView
	let _from: CGPoint
	let _to: CGPoint
	
	init(node: LinkableView, from: CGPoint, to: CGPoint) {
		self._node = node
		self._from = from
		self._to = to
	}
	
	func execute() {
		_node.move(to: _to)
	}
	
	func unexecute() {
		_node.move(to: _from)
	}
}
